package day07;

public class CoffeeMachine {
	private int coffee;
	private int cream;
	private int sugar;
	public int getCream() {
		return cream;
	}
	public void setCream(int cream) {
		this.cream = cream;
	}
	public int getSugar() {
		return sugar;
	}
	public void setSugar(int sugar) {
		this.sugar = sugar;
	}
	public void setCoffee(int c) {
		coffee=c;
	}
	public int getCoffee() {
		return coffee;
	}
}
